       Real Madrid adalah klub sepak bola profesional yang berbasis di Madrid, Spanyol. Klub ini didirikan pada tahun 1902 dan telah menjadi salah satu klub terbesar dan paling sukses di dunia sepak bola. Real Madrid dikenal dengan julukan "Los Blancos" atau "Los Merengues" karena seragam putih mereka yang ikonik.
<br><br>
Real Madrid telah memenangkan berbagai gelar domestik dan internasional, termasuk 34 gelar La Liga Spanyol, 19 gelar Piala Raja Spanyol, dan 13 gelar Liga Champions UEFA. Klub ini juga memenangkan beberapa gelar Piala Super UEFA, Piala Interkontinental, dan Piala Dunia Antarklub FIFA.
<br><br>
Real Madrid juga dikenal dengan tim dan pemain legendarisnya, termasuk Ferenc Puskas, Alfredo Di Stefano, Zinedine Zidane, Ronaldo, dan Cristiano Ronaldo. Klub ini memiliki rivalitas sengit dengan Barcelona, dikenal sebagai "El Clasico", yang dianggap sebagai salah satu pertandingan sepak bola paling bergengsi di dunia.
<br><br>
Real Madrid bermain di Stadion Santiago Bernabeu yang memiliki kapasitas sekitar 81.000 penonton. Klub ini dimiliki oleh anggota pendiri dan para pemegang saham yang berjumlah sekitar 90.000 orang. Presiden saat ini adalah Florentino Perez, yang telah memimpin klub dalam beberapa periode masa jabatan yang berbeda.
<br><br>
Real Madrid memiliki akademi sepak bola yang terkenal, La Fabrica, yang telah melahirkan banyak pemain terbaik di dunia, seperti Raul Gonzalez, Iker Casillas, dan Sergio Ramos. Klub ini juga terkenal dengan filosofi permainan menyerang dan atraktif, serta kebijakan transfer yang ambisius untuk membangun tim terbaik di dunia.

